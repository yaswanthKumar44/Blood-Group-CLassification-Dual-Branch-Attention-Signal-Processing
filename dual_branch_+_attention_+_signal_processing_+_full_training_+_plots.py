    running_loss = 0

    for raw, signal, y in tqdm(train_dl):
        raw, signal, y = raw.to(device), signal.to(device), y.to(device)

        optimizer.zero_grad()
        out = model(raw, signal)
        loss = criterion(out, y)

        loss.backward()
        optimizer.step()

        running_loss += loss.item()
        tp += out.argmax(1).cpu().tolist()
        ty += y.cpu().tolist()

    scheduler.step()
    lr_hist.append(optimizer.param_groups[0]["lr"])

    train_loss = running_loss / len(train_dl)
    train_acc = accuracy_score(ty, tp)

    # VALIDATION
    model.eval()
    vp, vy = [], []
    val_loss = 0

    with torch.no_grad():
        for raw, signal, y in val_dl:
            raw, signal, y = raw.to(device), signal.to(device), y.to(device)
            out = model(raw, signal)
            loss = criterion(out, y)

            val_loss += loss.item()
            vp += out.argmax(1).cpu().tolist()
            vy += y.cpu().tolist()

    val_loss /= len(val_dl)
    val_acc = accuracy_score(vy, vp)

    train_acc_hist.append(train_acc)
    val_acc_hist.append(val_acc)
    train_loss_hist.append(train_loss)
    val_loss_hist.append(val_loss)

    print(f"Epoch {epoch+1}: Train={train_acc:.4f} | Val={val_acc:.4f}")

    if val_acc > best_val:
        best_val = val_acc
        torch.save(model.state_dict(),"best_dual_model.pth")
