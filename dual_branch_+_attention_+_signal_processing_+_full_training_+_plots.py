def debug_signal_pipeline(img):
    gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)

    clahe = cv2.createCLAHE(2.0,(8,8))
    clahe_img = clahe.apply(gray)

    blur = cv2.GaussianBlur(clahe_img, (3,3), 0)

    sobelx = cv2.Sobel(blur, cv2.CV_64F, 1, 0)
    sobely = cv2.Sobel(blur, cv2.CV_64F, 0, 1)
    magnitude = cv2.magnitude(sobelx, sobely)

    norm = cv2.normalize(magnitude, None, 0, 255, cv2.NORM_MINMAX)

    return gray, clahe_img, blur, magnitude, norm

