class DualDataset(Dataset):
    def __init__(self, files, labels, tfm):
        self.files = files
        self.labels = labels
        self.tfm = tfm

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        img = np.array(Image.open(self.files[idx]).convert("RGB"))

        raw_img = img.copy()
        signal_img = enhance_fingerprint(img)

        raw_img = self.tfm(image=raw_img)["image"]
        signal_img = self.tfm(image=signal_img)["image"]

        return raw_img, signal_img, torch.tensor(self.labels[idx])

