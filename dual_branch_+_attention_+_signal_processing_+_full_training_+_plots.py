class AttentionFusion(nn.Module):
    def __init__(self, dim):
        super().__init__()
        self.attn = nn.Sequential(
            nn.Linear(dim*2, dim),
            nn.ReLU(),
            nn.Linear(dim, 2),
            nn.Softmax(dim=1)
        )

    def forward(self, f1, f2):
        weights = self.attn(torch.cat([f1,f2], dim=1))
        w1 = weights[:,0].unsqueeze(1)
        w2 = weights[:,1].unsqueeze(1)
        return w1*f1 + w2*f2

