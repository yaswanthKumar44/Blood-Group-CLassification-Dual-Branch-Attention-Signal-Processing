import numpy as np
import matplotlib.pyplot as plt
from PIL import Image
import torch
from pytorch_grad_cam import GradCAM
from pytorch_grad_cam.utils.image import show_cam_on_image

# =========================================================
# Wrapper for GradCAM (dual input fix)
# =========================================================
class GradCAMWrapper(torch.nn.Module):
    def __init__(self, model, signal):
        super().__init__()
        self.model = model
        self.signal = signal

    def forward(self, x):
        return self.model(x, self.signal)

# =========================================================
# FUNCTION: VISUALIZE COMBINED RESULTS
# =========================================================
def visualize_combined(model, files, n=5):
    model.eval()
    np.random.seed(42)

    sample_paths = np.random.choice(files, size=min(n, len(files)), replace=False)

    plt.figure(figsize=(12, 4*n))
    plt.suptitle("Original vs Enhanced vs Grad-CAM", fontsize=16)

    for i, path in enumerate(sample_paths):
        # Load image
        original = np.array(Image.open(path).convert("RGB"))

        # Signal enhanced
        enhanced = enhance_fingerprint(original)

        # Preprocess
        raw_tensor = val_tfms(image=original)["image"].unsqueeze(0).to(device)
        signal_tensor = val_tfms(image=enhanced)["image"].unsqueeze(0).to(device)

        # Wrapper
        wrapped_model = GradCAMWrapper(model, signal_tensor)

        # Target layer (EfficientNet last block)
        target_layer = model.raw.blocks[-1]

        cam = GradCAM(model=wrapped_model, target_layers=[target_layer])

        grayscale_cam = cam(input_tensor=raw_tensor)[0]

        # Prepare image for overlay
        raw_img = raw_tensor.squeeze().permute(1,2,0).cpu().numpy()
        raw_img = (raw_img - raw_img.min()) / (raw_img.max() - raw_img.min())

        cam_image = show_cam_on_image(raw_img, grayscale_cam, use_rgb=True)

        # Prediction
        with torch.no_grad():
            out = model(raw_tensor, signal_tensor)
            prob = torch.softmax(out, dim=1)
            pred = prob.argmax(1).item()
            conf = prob.max().item()

        # Plot Original
        plt.subplot(n, 3, i*3 + 1)
        plt.imshow(original)
        plt.title("Original")
        plt.axis("off")

        # Plot Enhanced
        plt.subplot(n, 3, i*3 + 2)
        plt.imshow(enhanced)
        plt.title("Enhanced")
        plt.axis("off")

        # Plot Grad-CAM
        plt.subplot(n, 3, i*3 + 3)
        plt.imshow(cam_image)
        plt.title(f"Grad-CAM\n{classes[pred]} ({conf:.2f})")
        plt.axis("off")

    plt.tight_layout(rect=[0, 0, 1, 0.96])
    plt.savefig("combined_visualization.png", dpi=300)
    plt.show()

