class DualBranchNet(nn.Module):
    def __init__(self, num_classes):
        super().__init__()

        self.raw = timm.create_model("tf_efficientnetv2_s", pretrained=True, num_classes=0)
        self.signal = timm.create_model("tf_efficientnetv2_s", pretrained=True, num_classes=0)

        dim = self.raw.num_features

        self.fusion = AttentionFusion(dim)

        self.fc = nn.Sequential(
            nn.Linear(dim,512),
            nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Dropout(0.4),
            nn.Linear(512,num_classes)
        )

    def forward(self, x1, x2):
        f1 = self.raw(x1)
        f2 = self.signal(x2)
        fused = self.fusion(f1,f2)
        return self.fc(fused)

